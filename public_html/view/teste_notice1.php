        <p class="desc_noticia"> A normalização no abastecimento dos postos de combustíveis de São Paulo deve durar pelo menos três dias, segundo as empresas distribuidoras. Caminhões-tanque voltaram a fazer a distribuição nesta quarta-feira (7), após o sindicato que representa os caminhoneiros autônomos decidir acatar a decisão judicial que determinava a retomada do trabalho. Entretanto, os veículos que fazem as entregas estão saindo às ruas ainda com escolta policial.
    Entre a noite desta quarta e a madrugada desta quinta-feira (8), caminhões abasteceram postos na Zona Sul, como mostrou o Bom Dia São Paulo. Por volta das 22h30, havia longas filas em um posto na Avenida Jornalista Roberto Marinho que recebeu combustível. <br />"Meu carro estava bem na reserva já, tive que sair com o da minha mãe, que ainda tinha um pouco de gasolina", contou o empresário Rodrigo Monteiro de Castro.<br /><br /> Desde segunda, os motoristas de caminhões-tanque paralisaram as atividades em protesto contra as restrições de circulação na Marginal Tietê e em outras vias do Minianel Viário. Os caminhões estão proibidos de trafegar nessas vias das 5h às 9h e das 17h às 22h de segunda a sexta e das 10h às 14h aos sábados.</p>
    
	    	
